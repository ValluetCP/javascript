//  Les 5 opérations : 
// - Déclaration d'une variable et affectation d'une valeur
// - Conditions 
// - Exécution 
// - Incrémentation

var k = 0; /*déclaration et affectation*/
while (k < couleurs.lenght) /*condition*/ {
    document.write("<p>" + couleurs[k] + "</p>"); /*éxécution*/
    k++; /*incrémentation*/
}




